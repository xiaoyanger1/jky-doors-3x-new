        /// <summary>
        /// 字符型转Decimal (出错返回0)
        /// </summary>
        /// <param name="strValue">输入字符串</param>
        /// <returns></returns>
        public static decimal _Stringt2Decimal(string strValue)
        {
            try
            {
                return decimal.Parse(strValue);
            }
            catch
            {
                return 0;
            }
        }

        #endregion

        #region (public) Decimal 精确到整数，只入不舍。_Decimal2Decimal

        /// <summary>
        /// Decimal按小数第一位只入不舍，例如：参数为10.1 则返回11 (异常出错返回0)
        /// </summary>
        /// <param name="strValue">输入字符串</param>
        /// <returns></returns>
        public static decimal _Decimal2Decimal(decimal dcmValue)
        {
            try
            {
                decimal dcmWholeNumber= Math.Round(dcmValue, 0);
                if (dcmWholeNumber<dcmValue)
                    return dcmWholeNumber+1;
                else return dcmWholeNumber;
            }
            catch
            {
                return 0;
            }
        }

        #endregion

        #region (public) 字符型转布尔型 _String2Bool

        /// <summary>
        /// 字符型转布尔型 (错误返回 false)
        /// </summary>
        /// <param name="strValue">输入值</param>
        /// <returns></returns>
        public static bool _String2Bool(string strValue)
        {
            try
            {
                return Boolean.Parse(strValue);
            }
            catch
            {
                return false;
            }
        }

        #endregion

        #region （public）将DateTime时间格式转换为Unix时间戳格式 _ConvertDateTimeInt
        /// <summary>
        /// 将c# DateTime时间格式转换为Unix时间戳格式
        /// </summary>
        /// <param name="time">时间</param>
        /// <returns>double</returns>
        public static long _ConvertDateTimeInt(System.DateTime time)
        {
            double intResult = 0;
            System.DateTime startTime = TimeZone.CurrentTimeZone.ToLocalTime(new System.DateTime(1970, 1, 1));
            intResult = (time - startTime).TotalSeconds;
            return (long)intResult;
        }
        #endregion

        #region （public） Unix 时间戳转换成 DateTime时间格式 _UTCSecondsToDataTime
        /// <summary>
        /// 将c# Unix 时间戳转换成 DateTime时间格式
        /// </summary>
        /// <param name="seconds">秒数</param>
        /// <returns>double</returns>
        public static DateTime _UTCSecondsToDataTime(long seconds)
        {
            return new System.DateTime(1970, 1, 1).ToLocalTime().AddSeconds(seconds);
        }
