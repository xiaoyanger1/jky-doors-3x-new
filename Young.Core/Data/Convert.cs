    /// <summary>
    /// 枚举转成Javascript
    /// </summary>
    public static class EnumExtensions
    {
        public static string ToScript(this Type enumType)
        {

            int[] values = (int[])Enum.GetValues(enumType);
            string[] names = Enum.GetNames(enumType);
            string[] pairs = new string[values.Length];

            for (int i = 0; i < values.Length; i++)
            {
                pairs[i] = names[i] + ": " + values[i];
            }

            return string.Format("var e{0}={{\n{1}\n}}", enumType.Name, string.Join(",\n", pairs));
        }

        public static Dictionary<string, string> ToDictionary(this Type enumType)
        {
            int[] values = (int[])Enum.GetValues(enumType);
            string[] names = Enum.GetNames(enumType);

            Dictionary<string, string> dic = new Dictionary<string, string>();

            for (int i = 0; i < values.Length; i++)
            {
                dic.Add(values[i].ToString(), names[i].ToString());
            }
            return dic;
        }
    }

